import pandas as pd
import numpy as np
import seaborn as sns
import matplotlib.pyplot as plt
df = pd.read_csv('./Medical Data Visualizer/medical_examination.csv')
df

# # Add an overweight column to the data. To determine if a person is overweight, first calculate their BMI by dividing their weight in kilograms by the square of
# their height in meters. If that value is > 25 then the person is overweight. Use the value 0 for NOT overweight and the value 1 for overweight.

df['overweight'] = np.where(df['weight']/(df['height']/100)**2>25,1,0)
df['overweight']

# Normalize data by making 0 always good and 1 always bad. If the value of cholesterol or gluc is 1, set the value to 0. If the value is more than 1, set the value
# to 1.

df['cholesterol'] = np.where(df['cholesterol']>1,0,1)
df['gluc'] = np.where(df['gluc']>1,0,1)
df

# Draw the Categorical Plot in the draw_cat_plot function.

def draw_cat_plot():
  df_cat = pd.melt(df, id_vars=['cardio'], value_vars=['cholesterol','gluc','smoke','alco','active','overweight'])
  df_cat = df_cat.groupby(['cardio','variable','value']).size().reset_index(name='total')
  graph = sns.catplot(
      x='variable',
      y='total',
      hue='value',
      col='cardio',
      data=df_cat,
      kind='bar'
  )

  fig = graph.fig

  fig.savefig('catplot.png')
  return fig
draw_cat_plot()

# Draw the Heat Map in the draw_heat_map function.

def draw_heat_map():
  df_heat =df[
      (df['ap_lo'] <= df['ap_hi']) &
      (df['height'] >= df['height'].quantile(0.025)) &
      (df['height'] <= df['height'].quantile(0.975)) &
      (df['weight'] >= df['weight'].quantile(0.025)) &
      (df['weight'] <= df['weight'].quantile(0.975))
  ]

  corr = df_heat.corr()
  mask = np.triu(np.ones_like(corr, dtype=bool))

  fig, ax = plt.subplots(figsize=(12,12))

  sns.heatmap(
      corr,
      mask=mask,
      annot=True,
      fmt='.1f',
      square=True,
      linewidths=.5,
      cbar_kws={'shrink': .5},
      ax=ax
  )

  fig.savefig('heatmap.png')
  return fig

draw_heat_map()