# Use Pandas to import the data from epa-sea-level.csv.
# Use matplotlib to create a scatter plot using the Year column as the x-axis and the CSIRO Adjusted Sea Level column as the y-axis.

import pandas as pd
import matplotlib.pyplot as plt
from scipy.stats import linregress

def draw_plot():
  df = pd.read_csv('epa-sea-level.csv')
  fig, ax = plt.subplots(figsize=(10,6))

  ax.scatter(df['Year'], df['CSIRO Adjusted Sea Level'], color='blue', label='Historical Data')

  res = linregress(df['Year'], df['CSIRO Adjusted Sea Level'])

  years_extended = pd.Series([i for i in range(1880, 2051)])

  sea_levels_predicted = res.slope * years_extended + res.intercept

  ax.plot(years_extended, sea_levels_predicted, color='red', label='Best Fit Line 1')

  df_recent = df[df['Year'] >= 2000]

  res_recent = linregress(df_recent['Year'], df_recent['CSIRO Adjusted Sea Level'])

  years_recent = pd.Series([i for i in range(2000, 2051)])

  sea_levels_recent = res_recent.slope * years_recent + res_recent.intercept

  ax.plot(years_recent, sea_levels_recent, color='green', label='Best Fit Line 2')

  ax.set_title('Rise in Sea Level')
  ax.set_xlabel('Year')
  ax.set_ylabel('Sea Level (inches)')

  plt.savefig('sea_level_plot.png')
  return ax
draw_plot()

