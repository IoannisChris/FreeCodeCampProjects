import pandas as pd
df = pd.read_csv('adult.data.csv')
df.head()

# How many people of each race are represented in this dataset?
# This should be a Pandas series with race names as the index labels. (race column)

race_count = df.race.value_counts()
race_count

# What is the average age of men?

avg_men_age = df[df['sex'] == 'Male'].age.mean()
avg_men_age

# What is the percentage of people who have a Bachelor's degree?

total_people = df.shape[0]
bachelor_people = len(df[df['education'] == 'Bachelors'])
percentage_bachelors = round((bachelor_people/total_people) * 100, 1)
percentage_bachelors

#What percentage of people with advanced education
#(Bachelors, Masters, or Doctorate) make more than 50K?

higher_degrees = ['Bachelors','Masters','Doctorate']
higher_education = df[df['education'].isin(higher_degrees)]
educated_withmorethan_50K = higher_education[higher_education['salary'] == '>50K']
percentage_higher_degrees_morethan_50k = round((len(educated_withmorethan_50K)/len(higher_education)) * 100, 1)
percentage_higher_degrees_morethan_50k

# What percentage of people without advanced education make more than 50K?

lower_education = df[~df['education'].isin(higher_degrees)]
noeducated_withmorethan_50K = lower_education[lower_education['salary'] == '>50K']
percentage_no_degrees_morethan_50K = round((len(noeducated_withmorethan_50K)/len(lower_education)) * 100, 1)
percentage_no_degrees_morethan_50K

# What is the minimum number of hours a person works per week?

min_hours = df['hours-per-week'].min()
min_hours

# What percentage of the people who work the minimum number of hours per week have
# a salary of more than 50K?

count_how_many_work_1hour = df[df['hours-per-week'] == min_hours]
hour_and_morethan_50K = count_how_many_work_1hour[count_how_many_work_1hour['salary'] == '>50K']
percentage_hour_and_morethan_50K = round((len(hour_and_morethan_50K)/len(count_how_many_work_1hour)) * 100, 1)
percentage_hour_and_morethan_50K

# What country has the highest percentage of people that earn >50K and what is that
# percentage?

people_per_country = df['native-country'].value_counts()
rich_people_per_country = df[df['salary'] == '>50K']['native-country'].value_counts()
country_percentages = (rich_people_per_country / people_per_country) * 100
highest_earning_country = country_percentages.idxmax()
highest_earning_country_percentage = round(country_percentages.max(), 1)
print(f"The country with the highest percentage of people earn >50K is {highest_earning_country} with {highest_earning_country_percentage}%")

# Identify the most popular occupation for those who earn >50K in India.

rich_indians = df[(df['native-country'] == 'India') & (df['salary'] == '>50K')]
occupation_counts = rich_indians['occupation'].value_counts()
top_IN_occupation = occupation_counts.idxmax()
print(f"The most popular occupation for high earners in India is: {top_IN_occupation}")
