# Use Pandas to import the data from "fcc-forum-pageviews.csv". Set the index to the date column.
# Clean the data by filtering out days when the page views were in the top 2.5% of the dataset or bottom 2.5% of the dataset.

import matplotlib.pyplot as plt
import pandas as pd
import seaborn as sns
from pandas.plotting import register_matplotlib_converters
register_matplotlib_converters()

df = pd.read_csv('fcc-forum-pageviews.csv', index_col='date', parse_dates=True)
df = df[
    (df['value'] >= df['value'].quantile(0.025)) &
    (df['value'] <= df['value'].quantile(0.975))
]

df

# Create a draw_line_plot function that uses Matplotlib to draw a line chart similar to "examples/Figure_1.png". The title should be Daily freeCodeCamp Forum Page
# Views 5/2016-12/2019. The label on the x axis should be Date and the label on the y axis should be Page Views.

def draw_line_plot():
  fig, ax = plt.subplots(figsize=(15, 5))

  ax.plot(df.index, df['value'], color='red', linewidth=1)

  ax.set_title('Daily freeCodeCamp Forum Page Views 5/2016-12/2019')
  ax.set_xlabel('Date')
  ax.set_ylabel('Page Views')

  fig.savefig('line_plot.png')
  return fig
draw_line_plot()

# Create a draw_bar_plot function that draws a bar chart similar to "examples/Figure_2.png". It should show average daily page views for each month grouped by year.
# The legend should show month labels and have a title of Months. On the chart, the label on the x axis should be Years and the label on the y axis should be
# Average Page Views.

def draw_bar_plot():
  df_bar = df.copy()

  df_bar['year'] = df_bar.index.year
  df_bar['month'] = df_bar.index.month_name()

  df_bar = df_bar.groupby(['year', 'month'])['value'].mean()

  df_bar = df_bar.unstack()

  months = ['January', 'February', 'March', 'April', 'May', 'June',
            'July', 'August', 'September', 'October', 'November', 'December']
  df_bar = df_bar.reindex(columns=months)

  fig = df_bar.plot(kind='bar', figsize=(10, 6)).figure

  plt.xlabel('Years')
  plt.ylabel('Average Page Views')
  plt.legend(title='Months')

  fig.savefig('bar_plot.png')
  return fig
draw_bar_plot()

# Create a draw_box_plot function that uses Seaborn to draw two adjacent box plots similar to "examples/Figure_3.png". These box plots should show how the values are
# distributed within a given year or month and how it compares over time. The title of the first chart should be Year-wise Box Plot (Trend) and the title of the
# second chart should be Month-wise Box Plot (Seasonality). Make sure the month labels on bottom start at Jan and the x and y axis are labeled correctly. The
# boilerplate includes commands to prepare the data.

def draw_box_plot():
  df_box = df.copy()
  df_box.reset_index(inplace=True)
  df_box['year'] = [d.year for d in df_box.date]
  df_box['month'] = [d.strftime('%b') for d in df_box.date]

  fig, axes = plt.subplots(nrows=1, ncols=2, figsize=(20,7))

  sns.boxplot(x='year', y='value', data=df_box, ax=axes[0])
  axes[0].set_title('Year-wise Box Plot (Trend)')
  axes[0].set_xlabel('Year')
  axes[0].set_ylabel('Page Views')

  month_order = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec']
  sns.boxplot(x='month', y='value', data=df_box, order=month_order, ax=axes[1])

  axes[1].set_title('Month-wise Box Plot (Seasonality)')
  axes[1].set_xlabel('Month')
  axes[1].set_ylabel('Page Views')

  fig.savefig('box_plot.png')
  return fig
draw_box_plot()